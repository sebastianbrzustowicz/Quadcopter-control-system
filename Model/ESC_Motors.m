function output = ESC_Motors(input)
PWM1 = input(1);
PWM2 = input(2);
PWM3 = input(3);
PWM4 = input(4);

% global 

F1 = 0.003537*(PWM1*390/1000-390)^2 + 1.225*(PWM1*390/1000-390);
F2 = 0.003537*(PWM2*390/1000-390)^2 + 1.225*(PWM2*390/1000-390);
F3 = 0.003537*(PWM3*390/1000-390)^2 + 1.225*(PWM3*390/1000-390);
F4 = 0.003537*(PWM4*390/1000-390)^2 + 1.225*(PWM4*390/1000-390);

% thrust (g) to force (N)

F1 = F1*9.81/1000;
F2 = F2*9.81/1000;
F3 = F3*9.81/1000;
F4 = F4*9.81/1000;

output = [F1; F2; F3; F4];
end